public class Arithmetic {
    public static void main(String[] args) {
        int a = 25, b = 13;
        System.out.print("First number: "+a+"\n");
        System.out.print("Second number: "+b+"\n");
        System.out.println("a + b = " + (a + b));
        System.out.println("a - b = " + (a - b));
        System.out.println("a * b = " + (a * b));
        System.out.println("a / b = " + (a / b));
        System.out.println("a % b = " + (a % b));
    }
}
